# ============================================================================

. /opt/nbdt/lib/common.sh

header "System Information"

##############################################################################
# Uptime
##############################################################################

line
echo "Uptime"
line

uptime

##############################################################################
# CPU Frequency
##############################################################################

echo
line
echo "CPU Frequency"
line

CPU=$(cat /sys/devices/system/cpu/cpufreq/policy0/scaling_cur_freq 2>/dev/null)

if [ -n "$CPU" ]; then
    printf "%d MHz\n" $((CPU / 1000))
else
    echo "Unavailable"
fi

##############################################################################
# Load Average
##############################################################################

echo
line
echo "Load Average"
line

awk '{
    printf "1 min : %s\n", $1
    printf "5 min : %s\n", $2
    printf "15 min: %s\n", $3
}' /proc/loadavg

##############################################################################
# Memory Information
##############################################################################

echo
line
echo "Memory"
line

free

##############################################################################
# Running Processes
##############################################################################

echo
line
echo "Running Processes"
line

printf "%-15s : %s\n" "Processes" "$(ps | wc -l)"

##############################################################################
# Filesystem Usage
##############################################################################

echo
line
echo "Filesystem Usage"
line

df -h

##############################################################################
# End of File
##############################################################################
